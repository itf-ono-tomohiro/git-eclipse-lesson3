package ex1;

public class DisplayMonth {

	public static void main(String[] args) {
		// 数字を月に変換するプログラム
		int month = 2;
		switch(month) {
		case 1: 
			System.out.println("January");
			break;
		case 2:
			System.out.println("February");
			break;
		case 3:
			System.out.println("Narch");
			break;
		case 4:
			System.out.println("April");
			break;
		case 5:
			System.out.println("May");
			break;
		default:
			break;
			
		}

	}

}
